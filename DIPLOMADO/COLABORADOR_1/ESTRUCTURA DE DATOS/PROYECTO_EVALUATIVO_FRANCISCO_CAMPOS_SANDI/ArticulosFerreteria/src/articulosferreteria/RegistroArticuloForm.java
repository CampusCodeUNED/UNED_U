    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String nombre = nombreTextField.getText();
            if (nombre.isEmpty()) {
                JOptionPane.showMessageDialog(this, "El campo nombre no puede estar vacío.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            double precio = Double.parseDouble(precioTextField.getText());
            if (precio <= 0) {
                JOptionPane.showMessageDialog(this, "El precio debe ser un número positivo y mayor a 0.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            int id = Integer.parseInt(idTextField.getText());

            Articulo articulo = new Articulo(id, nombre, precio);
            listaArticulos.insertar(articulo);
            actualizarListaArticulos();

            generarIdArticulo();
            nombreTextField.setText("");
            precioTextField.setText("");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese un precio válido.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

