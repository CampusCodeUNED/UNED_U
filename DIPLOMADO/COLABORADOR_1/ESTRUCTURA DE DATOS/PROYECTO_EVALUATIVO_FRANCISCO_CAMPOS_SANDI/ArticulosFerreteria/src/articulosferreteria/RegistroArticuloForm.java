    private int generarIdUnico() {
        int id;
        boolean idUnico;
        do {
            id = (int) (Math.random() * 900) + 100;
            idUnico = listaArticulos.buscarPorId(id) == null;
        } while (!idUnico);
        return id;
    }

    private void generarIdArticulo() {
        int id = generarIdUnico();
        idTextField.setText(String.valueOf(id));
    }

